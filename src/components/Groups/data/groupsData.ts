// Helper functions

export function getGroupById(id: string): Group | undefined {
  return groups.find((group) => group.id === id);
}

export function getGroupsByCategory(
  category: "university" | "hsc" | "jobs" | "others"
): Group[] {
  return groups.filter((group) => group.categories?.includes(category));
}

export function getGroupsByUniversity(
  university: "buet" | "kuet" | "ruet" | "cuet"
): Group[] {
  return groups.filter((group) => group.university?.name === university);
}

export function getActiveGroups(): Group[] {
  return groups.filter((group) => group.status === "active");
}
